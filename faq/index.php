    <p>Is it hard to learn to fly?</p>
  </div>

  <div class = "answer">
    <p>No. People of all shapes and sizes, ages and abilities have learned to fly. It's fun, and from the beginning of your training, you get to do most of the actual flying! On the practical side — while flying isn't a difficult skill to learn, you'll have to be willing to stick with it until you meet all the requirements. Also, you should consider the cost of becoming a pilot — you'll have to pay for your physical exam, books  and your lessons.</p>
  </div>

  <div class = "question">
    <p>When can I start?</p>
  </div>

  <div class = "answer">
    <p>Right away!  You don't have to have a student pilot's certificate or a medical certificate to take flying lessons, but you will need them before you will be approved to fly solo.</p>
  </div>

   <div class = "question">
    <p>How many lessons do I have to take before I solo?</p>
  </div>

  <div class = "answer">
    <p>It depends on you. There is no set number of lessons or hours of flight training. Your instructor must make sure you have learned to perform certain maneuvers before allowing you to solo. These maneuvers include safe takeoffs and landings. You must use good judgment when flying and be able to keep control of the aircraft.</p>
    <p>Also, you'll have to get a medical certificate and a student pilot's certificate to fly solo.</p>
  </div>

